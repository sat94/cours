# print() : Utilisée pour afficher des informations à la sortie standard (généralement la console ou le terminal). Accepte un ou plusieurs arguments et les affiche sous forme de texte.

# input() : Utilisée pour obtenir une entrée de l'utilisateur depuis la console. Affiche un message (optionnel) à l'utilisateur et attend une saisie au clavier, qui est généralement stockée dans une variable.

# len() : Utilisée pour obtenir la longueur d'une séquence (chaîne de caractères, liste, tuple, etc.). Renvoie le nombre d'éléments dans la séquence.

# range() : Utilisée pour créer une séquence d'entiers dans un certain intervalle. Souvent utilisée dans les boucles for pour itérer sur une séquence d'entiers.

# type() : Utilisée pour vérifier le type d'un objet ou d'une variable. Renvoie le type de l'objet passé en argument.

# int() : Utilisée pour convertir une valeur en un entier (int). Prend en argument une chaîne de caractères ou un nombre décimal et renvoie la version entière.

# float() : Utilisée pour convertir une valeur en un nombre à virgule flottante (float). Prend en argument une chaîne de caractères ou un entier et renvoie la version en virgule flottante.

# str() : Utilisée pour convertir une valeur en une chaîne de caractères (string). Prend en argument un nombre ou un autre type d'objet et renvoie la version en chaîne de caractères.

# list() : Utilisée pour créer une liste à partir d'une séquence d'éléments. Prend en argument une séquence (comme un tuple ou une chaîne de caractères) et renvoie une liste.

# tuple() : Utilisée pour créer un tuple à partir d'une séquence d'éléments. Prend en argument une séquence (comme une liste ou une chaîne de caractères) et renvoie un tuple.

# dict() : Utilisée pour créer un dictionnaire (collection d'associations clé-valeur) à partir de paires clé-valeur fournies en arguments.

# set() : Utilisée pour créer un ensemble (collection d'éléments uniques) à partir d'une séquence d'éléments.

# max() : Utilisée pour trouver la valeur maximale dans une séquence (liste, tuple ou chaîne de caractères).

# min() : Utilisée pour trouver la valeur minimale dans une séquence (liste, tuple ou chaîne de caractères).

# sum() : Utilisée pour calculer la somme des éléments d'une séquence numérique (liste, tuple, etc.).

# abs() : Utilisée pour obtenir la valeur absolue d'un nombre (c'est-à-dire sa valeur sans signe).

# round() : Utilisée pour arrondir un nombre à un nombre entier ou à un certain nombre de décimales.

# sorted() : Utilisée pour trier une séquence (liste, tuple, chaîne de caractères, etc.) dans un ordre spécifique.

# open() : Utilisée pour ouvrir un fichier en mode lecture, écriture ou autre. Elle retourne un objet de fichier que vous pouvez utiliser pour lire ou écrire des données dans le fichier.

# close() : Utilisée pour fermer un fichier ouvert avec open().

